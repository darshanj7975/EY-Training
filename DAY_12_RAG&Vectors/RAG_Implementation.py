def retrieve_pinecone(query, top_k=3):

    query_embedding = embedding_model.encode(query)

    results = index.query(
        vector=query_embedding.tolist(),
        top_k=top_k,
        include_metadata=True
    )

    contexts = []

    for match in results["matches"]:
        contexts.append(
            match["metadata"]["text"]
        )

    return contexts

