def evaluate_context_relevance(
        question,
        context):

    prompt = f"""
You are a RAG evaluator.

Question:
{question}

Retrieved Context:
{context}

Evaluate how relevant the context is
for answering the question.

Score from 1-5

5 = Highly Relevant
4 = Mostly Relevant
3 = Partially Relevant
2 = Slightly Relevant
1 = Irrelevant

Return exactly:

Score:
Reason:
"""

    return llm_judge(prompt)

def evaluate_answer_relevance(
        question,
        answer):

    prompt = f"""
Question:
{question}

Answer:
{answer}

Evaluate answer relevance.

Score from 1-5

Return:

Score:
Reason:
"""

    return llm_judge(prompt)

def evaluate_groundedness(
        context,
        answer):

    prompt = f"""
Retrieved Context:
{context}

Generated Answer:
{answer}

Determine whether the answer
is fully supported by the context.

Score:

5 = Fully Grounded
4 = Mostly Grounded
3 = Partially Grounded
2 = Weakly Grounded
1 = Hallucinated

Return:

Score:
Reason:
"""

    return llm_judge(prompt)

