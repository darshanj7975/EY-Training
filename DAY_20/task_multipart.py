import random
import time
import uuid
import json


class TelemetryListener:

    def __init__(self, total_pipeline_steps):

        self.trace_id = str(uuid.uuid4())

        self.total_pipeline_steps = total_pipeline_steps

        self.completed_steps = 0

        self.run_start = time.time()

        self.agent_start_times = {}

        self.last_progress_bucket = {}

        self.completed_agents = []

        self.run_status = "SUCCESS"

        self.failed_agent = None

    def emit(self, event):

        print(
            json.dumps(
                event,
                indent=2
            )
        )

    def agent_started(self, agent_name):

        span_id = str(uuid.uuid4())

        self.agent_start_times[agent_name] = {
            "span_id": span_id,
            "start_time": time.time()
        }

        self.emit(
            {
                "timestamp":
                    round(time.time(), 3),

                "trace_id":
                    self.trace_id,

                "span_id":
                    span_id,

                "event":
                    "agent_started",

                "agent":
                    agent_name
            }
        )

    def agent_progress(
        self,
        agent_name,
        step,
        total_steps
    ):

        percent = int(
            (
                step /
                total_steps
            ) * 100
        )

        bucket = (
            percent // 25
        ) * 25

        previous_bucket = (
            self.last_progress_bucket.get(
                agent_name,
                -1
            )
        )

        if bucket <= previous_bucket:
            return

        self.last_progress_bucket[
            agent_name
        ] = bucket

        self.completed_steps += 1

        elapsed = (
            time.time()
            - self.run_start
        )

        throughput = (
            self.completed_steps /
            elapsed
            if elapsed > 0
            else 0
        )

        pipeline_percent = (
            self.completed_steps /
            self.total_pipeline_steps
        ) * 100

        span_id = (
            self.agent_start_times[
                agent_name
            ]["span_id"]
        )

        self.emit(
            {
                "timestamp":
                    round(time.time(), 3),

                "trace_id":
                    self.trace_id,

                "span_id":
                    span_id,

                "event":
                    "agent_progress",

                "agent":
                    agent_name,

                "step":
                    step,

                "total_steps":
                    total_steps,

                "agent_percent":
                    percent,

                "pipeline_percent":
                    round(
                        pipeline_percent,
                        2
                    ),

                "throughput":
                    round(
                        throughput,
                        2
                    )
            }
        )

    def agent_completed(
        self,
        agent_name
    ):

        self.completed_agents.append(
            agent_name
        )

        info = (
            self.agent_start_times[
                agent_name
            ]
        )

        duration = (
            time.time()
            - info["start_time"]
        )

        self.emit(
            {
                "timestamp":
                    round(time.time(), 3),

                "trace_id":
                    self.trace_id,

                "span_id":
                    info["span_id"],

                "event":
                    "agent_completed",

                "agent":
                    agent_name,

                "duration_seconds":
                    round(
                        duration,
                        2
                    )
            }
        )

    def agent_failed(
        self,
        agent_name,
        step,
        error
    ):

        self.run_status = "FAILED"

        self.failed_agent = (
            agent_name
        )

        span_id = (
            self.agent_start_times[
                agent_name
            ]["span_id"]
        )

        pipeline_percent = (
            self.completed_steps /
            self.total_pipeline_steps
        ) * 100

        self.emit(
            {
                "timestamp":
                    round(time.time(), 3),

                "trace_id":
                    self.trace_id,

                "span_id":
                    span_id,

                "event":
                    "agent_failed",

                "agent":
                    agent_name,

                "failed_step":
                    step,

                "error":
                    str(error),

                "pipeline_percent":
                    round(
                        pipeline_percent,
                        2
                    )
            }
        )

    def run_summary(self):

        duration = (
            time.time()
            - self.run_start
        )

        self.emit(
            {
                "timestamp":
                    round(time.time(), 3),

                "trace_id":
                    self.trace_id,

                "event":
                    "run_summary",

                "status":
                    self.run_status,

                "total_duration_seconds":
                    round(
                        duration,
                        2
                    ),

                "agents_completed":
                    self.completed_agents,

                "failed_agent":
                    self.failed_agent
            }
        )


class Agent:

    def __init__(
        self,
        name,
        steps,
        fail_at_step=None
    ):

        self.name = name
        self.steps = steps
        self.fail_at_step = fail_at_step

    def run(
        self,
        listener
    ):

        listener.agent_started(
            self.name
        )

        for step in range(
            1,
            self.steps + 1
        ):

            time.sleep(
                random.uniform(
                    0.05,
                    0.2
                )
            )

            if (
                self.fail_at_step
                and
                step ==
                self.fail_at_step
            ):

                raise RuntimeError(
                    f"{self.name} failed at step {step}"
                )

            listener.agent_progress(
                self.name,
                step,
                self.steps
            )

        listener.agent_completed(
            self.name
        )


class Orchestrator:

    def __init__(
        self,
        agents,
        listener
    ):

        self.agents = agents
        self.listener = listener

    def run(self):

        for agent in self.agents:

            try:

                agent.run(
                    self.listener
                )

            except Exception as e:

                self.listener.agent_failed(
                    agent.name,
                    agent.fail_at_step,
                    e
                )

                break

        self.listener.run_summary()


def main():

    agents = [

        Agent(
            "Planner",
            3
        ),

        Agent(
            "Researcher",
            6
        ),

        Agent(
            "Writer",
            4,
            fail_at_step=2
        ),

        Agent(
            "Reviewer",
            2
        )
    ]

    total_steps = sum(
        agent.steps
        for agent in agents
    )

    listener = (
        TelemetryListener(
            total_steps
        )
    )

    Orchestrator(
        agents,
        listener
    ).run()


if __name__ == "__main__":
    main()