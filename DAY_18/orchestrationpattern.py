# Custom selector: Planner always goes first
def selector_func(messages):
    speakers = [getattr(m, "source", "") for m in messages]

    if "planner" not in speakers:
        return "planner"

    # After planner speaks once, let the LLM decide
    return None

# Stop infinite loops
