"""## Step 5 — Parse results into DataFrames"""

import pandas as pd

def parse_face_emotions(predictions):
    rows = []
    for pred in predictions:
        results = pred.get("results", {}).get("predictions", [])
        for file_pred in results:
            for model_pred in file_pred.get("models", {}).get("face", {}).get("grouped_predictions", []):
                face_id = model_pred.get("id", "face_0")
                for frame in model_pred.get("predictions", []):
                    t = frame.get("time", 0)
                    for emotion in frame.get("emotions", []):
                        rows.append({
                            "time":    round(t, 3),
                            "face_id": face_id,
                            "emotion": emotion["name"],
                            "score":   round(emotion["score"], 4),
                        })
    return pd.DataFrame(rows)


def parse_prosody_emotions(predictions):
    rows = []
    for pred in predictions:
        results = pred.get("results", {}).get("predictions", [])
        for file_pred in results:
            for segment in file_pred.get("models", {}).get("prosody", {}).get("grouped_predictions", []):
                for chunk in segment.get("predictions", []):
                    t_start = chunk.get("time", {}).get("begin", 0)
                    t_end   = chunk.get("time", {}).get("end",   0)
                    for emotion in chunk.get("emotions", []):
                        rows.append({
                            "time_start": round(t_start, 3),
                            "time_end":   round(t_end,   3),
                            "emotion":    emotion["name"],
                            "score":      round(emotion["score"], 4),
                        })
    return pd.DataFrame(rows)


def parse_language_emotions(predictions):
    rows = []
    for pred in predictions:
        results = pred.get("results", {}).get("predictions", [])
        for file_pred in results:
            for segment in file_pred.get("models", {}).get("language", {}).get("grouped_predictions", []):
                for chunk in segment.get("predictions", []):
                    text = chunk.get("text", "")
                    t    = chunk.get("position", {}).get("begin", 0)
                    for emotion in chunk.get("emotions", []):
                        rows.append({
                            "time":    round(t, 3),
                            "text":    text,
                            "emotion": emotion["name"],
                            "score":   round(emotion["score"], 4),
                        })
    return pd.DataFrame(rows)


