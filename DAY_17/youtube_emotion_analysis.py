import plotly.graph_objects as go
from plotly.subplots import make_subplots

def make_bar_chart(df, title, color):
    if df.empty:
        return None
    top = df.groupby("emotion")["score"].mean().sort_values(ascending=False).head(12)
    return go.Bar(x=top.index.tolist(), y=top.values.tolist(),
                  name=title, marker_color=color, text=[f"{v:.3f}" for v in top.values],
                  textposition="outside")

