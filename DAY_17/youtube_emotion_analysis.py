"""## Step 4 — Submit to Hume.ai for emotion analysis"""

import requests, json, time

HUME_API_BASE = "https://api.hume.ai/v0/batch"

def submit_job(api_key, video_path, models):
    """Upload video and start a Hume.ai batch job."""
    headers = {"X-Hume-Api-Key": api_key}

    # Build model config
    model_config = {}
    for m in models:
        model_config[m] = {}

    files   = {"file":   open(video_path, "rb")}
    payload = {"json":   json.dumps({"models": model_config})}

    print(f"Submitting to Hume.ai — models: {models}")
    resp = requests.post(f"{HUME_API_BASE}/jobs",
                         headers=headers,
                         data=payload,
                         files=files)
    resp.raise_for_status()
    job_id = resp.json()["job_id"]
    print(f"✅ Job submitted — ID: {job_id}")
    return job_id


def wait_for_job(api_key, job_id, poll_interval=5):
    """Poll until the job completes."""
    headers = {"X-Hume-Api-Key": api_key}
    url     = f"{HUME_API_BASE}/jobs/{job_id}"

    print("\nWaiting for analysis to complete...")
    while True:
        resp   = requests.get(url, headers=headers)
        resp.raise_for_status()
        data   = resp.json()
        status = data.get("state", {}).get("status", "unknown")

        print(f"  Status: {status}", end="\r")

        if status == "COMPLETED":
            print(f"\n✅ Analysis complete!")
            return data
        elif status in ("FAILED", "CANCELLED"):
            print(f"\n❌ Job {status}")
            print(json.dumps(data, indent=2))
            return None

        time.sleep(poll_interval)


def fetch_predictions(api_key, job_id):
    """Fetch the full predictions for a completed job."""
    headers = {"X-Hume-Api-Key": api_key}
    url     = f"{HUME_API_BASE}/jobs/{job_id}/predictions"
    resp    = requests.get(url, headers=headers)
    resp.raise_for_status()
    return resp.json()


