"""## Step 6 — Top emotions summary"""

def top_emotions(df, model_name, n=10):
    if df.empty:
        print(f"{model_name}: no data")
        return
    summary = (
        df.groupby("emotion")["score"]
        .mean()
        .sort_values(ascending=False)
        .head(n)
        .reset_index()
    )
    summary.columns = ["Emotion", "Avg Score"]
    summary["Avg Score"] = summary["Avg Score"].round(4)
    print(f"\n{'='*35}")
    print(f"  Top {n} — {model_name}")
    print(f"{'='*35}")
    print(summary.to_string(index=False))

top_emotions(df_face,     "Facial Expression")
top_emotions(df_prosody,  "Voice / Prosody")
top_emotions(df_language, "Language / Text")

