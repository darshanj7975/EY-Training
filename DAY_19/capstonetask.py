##Redis
import fakeredis
import json

redis_client = fakeredis.FakeStrictRedis()

class Memory:

    def save(self, role, content):

        redis_client.rpush(
            "history",
            json.dumps(
                {
                    "role": role,
                    "content": content
                }
            )
        )

    def load(self):

        return [
            json.loads(x)
            for x in redis_client.lrange(
                "history",
                0,
                -1
            )
        ]

mem = Memory()

mem.save(
    "user",
    "My shipping preference is express"
)

