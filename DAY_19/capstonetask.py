#Main Agent
async def agent(query):

    if not input_guardrail(query):

        return "Blocked"

    route = router(query)

    logger.info(
        f"Route={route}"
    )

    if route == "order":

        response = get_order(
            "A1002"
        )

    elif route == "policy":

        response = search_policy(
            query
        )

    elif route == "ticket":

        response = await create_ticket(
            query
        )

    else:

        response = (
            "General support"
        )

    return output_guardrail(
        str(response)
    )

