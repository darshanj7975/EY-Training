#Input Guardrial
BLOCKED = [
    "ignore instructions",
    "reveal customer data"
]

def input_guardrail(text):

    t = text.lower()

    for phrase in BLOCKED:

        if phrase in t:
            return False

    return True

#Output guardrials
import re

def output_guardrail(text):

    return re.sub(
        r"\S+@\S+",
        "[REDACTED]",
        text
    )
